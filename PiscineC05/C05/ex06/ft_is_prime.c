int	ft_is_prime(int nb)
{
	if (nb < 0 || nb == 0)
		return 0;
	if (nb % 2 == 0 && nb / 2 > 1)
		return (0);
	if (nb == 3)
		return (1);
	if (nb / nb == 1)
		return (1);
}
